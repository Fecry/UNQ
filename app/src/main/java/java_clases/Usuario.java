
package java_clases;

public class Usuario {
    int id;
    String contrasena;

    public Usuario() {
    }

    public Usuario(int id, String contrasena) {
        this.id = id;
        this.contrasena = contrasena;
    }

    public int getId() {
        return id;
    }

    public String getContrasena() {
        return contrasena;
    }

   
    
}
